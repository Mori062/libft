#include <stddef.h>
#include <stdio.h>
#include <string.h>

void	*memchr(const void *s, int c, size_t n)
{
	unsigned char	*tmp;

	tmp = (unsigned char *)s;
	while (n > 0)
	{
		if (tmp == c)
			return (tmp);
		tmp++;
		n--;
	}
	return (NULL);
}
